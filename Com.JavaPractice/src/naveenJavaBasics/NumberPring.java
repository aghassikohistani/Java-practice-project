package naveenJavaBasics;

public class NumberPring {

	public static void main(String[] args) {

		// 00 01 02 03 04
		// 10 11 12 13 14
		// 20 21 22 23 24

		for (int i = 0; i <= 9; i++) {

			for (int j = 0; j <= 9; j++) {
				System.out.print(i + "" + j + "");// 00 01
			}
			System.out.println();
		}

	}

}
